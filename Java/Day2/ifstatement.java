public class ifstatement {
    public static void main(String[] args){
        int x = 20;
        if(x>20){
            System.out.println("This is if statement");
        }
        else if(x<20){
            System.out.println("This is else if statement");
        }
        else{
            System.out.println("This is else statement");
        }
    }
}
